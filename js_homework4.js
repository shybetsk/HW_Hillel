// Функція, яка повертає випадкове ціле число від min до max (включно)
function randomInteger(min, max) {
    // Випадкове число між min і max включно
    let rand = min + Math.random() * (max + 1 - min);
    return Math.floor(rand); // Округлюємо вниз до цілого
}

// Функція для перевірки теорії ймовірності
function checkProbabilityTheory(count) {
    var even = 0; // лічильник парних чисел
    var odd = 0;  // лічильник непарних чисел

    // Генеруємо 'count' випадкових чисел і перевіряємо кожне з них
    for (var i = 0; i < count; i++) {
        var number = randomInteger(100, 1000); // генеруємо число від 100 до 1000 включно

        // Перевіряємо, чи число парне (якщо ділиться на 2 без остачі)
        if (number % 2 === 0) {
            even++; // додаємо до лічильника парних
        } else {
            odd++;  // додаємо до лічильника непарних
        }
    }

    // Загальна кількість чисел
    var total = even + odd;

    // Рахуємо відсоток парних і непарних чисел (округлюємо до 2 знаків після коми)
    var evenPercent = ((even / total) * 100).toFixed(2);
    var oddPercent = ((odd / total) * 100).toFixed(2);

    // Виводимо результати у консоль
    console.log("Кількість згенерованих чисел: " + total);
    console.log("Парних чисел: " + even)
    console.log("Непарних чисел: " + odd);
    console.log("Відсоток парних: " + evenPercent + "%");
    console.log("Відсоток непарних: " + oddPercent + "%");
}

// Викликаємо функцію з 1000 генераціями
checkProbabilityTheory(1000);


