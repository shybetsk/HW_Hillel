// Створюємо об'єкт services, у якому зберігаються назви послуг та їхня вартість з "грн"
var services = {
  "стрижка": "60 грн",
  "гоління": "80 грн",
  "Миття голови": "100 грн"
};

// Ще одна послуга
services['Розбити скло'] = "200 грн";

// Додаємо до об'єкта метод price, який рахує загальну вартість усіх послуг
services.price = function () {
  let sum = 0; // Змінна, в яку додається числова вартість кожної послуги

  // Перебираємо всі властивості об'єкта (включаючи додані пізніше)
  for (let key in this) {
    // Значення має бути рядком і містити "грн"
    if (typeof this[key] === "string" && this[key].includes("грн")) {
      // Витягуємо числову частину з рядка (наприклад, з "60 грн" отримаємо 60)
      let number = parseInt(this[key]);

      // Додаємо це число до загальної суми
      sum += number;
    }
  }

  // Повертаємо загальну суму з валютою "грн"
  return sum + " грн";
};

// Виводимо значення
console.log(services.price()); // "440 грн"

// Створюємо об'єкт services з назвами послуг і цінами у форматі рядків з "грн"
var services = {
  "стрижка": "60 грн",
  "гоління": "80 грн",
  "Миття голови": "100 грн",
  "Розбити скло": "200 грн"
};

//Метод, який повертає мінімальну ціну
services.minPrice = function () {
  let min = null; // Початкове значення: нічого ще не знайдено

  for (let key in this) { // Перебираємо всі послуги в об'єкті
    if (typeof this[key] === "string" && this[key].includes("грн")) {
      let value = parseInt(this[key]);

      // Якщо мінімум ще не заданий, або нове значення менше — оновлюємо
      if (min === null || value < min) {
        min = value;
      }
    }
  }

  return min + " грн"; // Повертаємо мінімальну ціну з валютою
};

//Виводимо мінімальне значення
console.log(services.minPrice());  // "60 грн"



//Метод, який повертає максимальну ціну
